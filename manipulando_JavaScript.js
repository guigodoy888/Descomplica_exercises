// Método cos()
var angulo = 20;
var cosseno = Math.cos(angulo);
console.log(cosseno);

// Método max()
var numeros = [40, 6, 35, 9];
var maxNumeros = Math.max(...numeros);
console.log(maxNumeros);

// Método min()
var numeros = [40, 6, 35, 9];
var minNumeros = Math.min(...numeros);
console.log(minNumeros);

// Método pow()
var base = 2;
var expoente = 10;
var resultado = Math.pow(base, expoente);
console.log(resultado);

// Método random()
var numAleatório = Math.random();
console.log(numAleatório);

// Método sqrt()
var numero = 36;
var raizQuadrada = Math.sqrt(numero);
console.log(raizQuadrada);
